package figuraQuadrataCodice;

public class Punto {
    private float x;
    private float y;
    
	
	  public Punto(float a,float b) { 
		  this.x=a; 
		  this.y=b; 
		  }
	 

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public void setY(float y) {
		this.y = y;
	}

	public float getY() {
		return y;
	}
}
